# ============================================
# Basic If-Else Statement
# ============================================
# KEY POINTS:
# - if statement executes code when condition is True
# - else provides alternative when condition is False
# - Proper indentation (4 spaces) is crucial in Python
# ============================================

is_kettle_boiled = True

if is_kettle_boiled:
    print("Kettle Done! Time to make tea.")
else:
    print("Kettle not boiled yet. Please wait.")
  